class JFormFieldAvatarFromComponent extends JFormField
{
	protected $type = 'avatarfromcomponent';

	function getInput() {
		$db = JFactory::getDBO();
        $sel_component = array();
        $sel_component['com_kunena'] = 'COM_JOOMLEAGUE_GLOBAL_AVATAR_FROM_KUNENA';
        $sel_component['com_cbe'] = 'COM_JOOMLEAGUE_GLOBAL_AVATAR_FROM_JOOMLA_CBE';
        $sel_component['com_comprofiler'] = 'COM_JOOMLEAGUE_GLOBAL_AVATAR_FROM_CB_ENHANCED';
        
        
        /*
        $lang = JFactory::getLanguage();
		$extension = "com_joomleague";
		$source = JPATH_ADMINISTRATOR . '/components/' . $extension;
		$lang->load("$extension", JPATH_ADMINISTRATOR, null, false, false)
		||	$lang->load($extension, $source, null, false, false)
		||	$lang->load($extension, JPATH_ADMINISTRATOR, $lang->getDefault(), false, false)
		||	$lang->load($extension, $source, $lang->getDefault(), false, false);
        */
		$mitems = array();
        $mitems[] = JHTML::_('select.option', 'prediction', JText::_('COM_JOOMLEAGUE_GLOBAL_AVATAR_FROM_PREDICTION'));
		$mitems[] = JHTML::_('select.option', 'com_users', JText::_('COM_JOOMLEAGUE_GLOBAL_AVATAR_FROM_JOOMLA'));
		
        foreach( $sel_component as $key => $value )
        {
            $query = "SELECT extension_id FROM #__extensions where type LIKE 'component' ";
            $query .= " and element like '".$key."'";
            $db->setQuery($query);
            if ( $result = $db->loadResult() )
            {
		$mitems[] = JHTML::_('select.option', $key , JText::_($value));
	       }
        
        }

		$output= JHTML::_('select.genericlist',  $mitems,
				$this->name,
				'class="inputbox" size="1"',
				'value', 'text', $this->value, $this->id);
		return $output;
	}
}